    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if(txtMaNganh.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Mã Ngành không được bỏ trống !!!","Lỗi",JOptionPane.ERROR_MESSAGE);
            //            txtThongBao.setText("Mã Ngành không được bỏ trống");
            txtMaNganh.setBackground(Color.red);
            txtMaNganh.requestFocus();
        }
        else if(txtTenNganh.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Tên ngành không được bỏ trống !!!","Lỗi",JOptionPane.ERROR_MESSAGE);
            //                txtThongBao.setText("Tên ngành không được bỏ trống");
            txtTenNganh.setBackground(Color.red);
            txtTenNganh.requestFocus();
        }
        else
        {
            try
            {
                Majors nganh = new Majors();
                nganh.setMa_Nganh(txtMaNganh.getText());
                nganh.setTen_Nganh(txtTenNganh.getText());

                MajorsDAO CSDl = new MajorsDAO();
                if(CSDl.insert(nganh))
                {
                    JOptionPane.showMessageDialog(null, "Thêm Ngành thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                    //                        txtThongBao.setText("Thêm Ngành thành công");
                    dataBang();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Thêm Ngành không thành công !!!","Lỗi",JOptionPane.ERROR_MESSAGE);
                    //                        txtThongBao.setText("Thêm Ngành không thành công");
                }
            }
            catch (Exception e)
            {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Trùng Mã Ngành !!!","Lỗi",JOptionPane.ERROR_MESSAGE);
                //                    txtThongBao.setText("Trùng Mã Ngành");
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

