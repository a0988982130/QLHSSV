    private void tblNganhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNganhMouseClicked
        try
        {
            int row = tblNganh.getSelectedRow();
            if(row>=0)
            {
                String id = (String)tblNganh.getValueAt(row, 0);
                MajorsDAO CSDL = new MajorsDAO();
                Majors nganh = CSDL.findByMa_Nganh(id);

                if(nganh != null)
                {
                    txtMaNganh.setText(nganh.getMa_Nganh());
                    txtTenNganh.setText(nganh.getTen_Nganh());
                }
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi !!!","Lỗi",JOptionPane.ERROR_MESSAGE);
            //            txtThongBao.setText("Lỗi");
        }
    }//GEN-LAST:event_tblNganhMouseClicked

